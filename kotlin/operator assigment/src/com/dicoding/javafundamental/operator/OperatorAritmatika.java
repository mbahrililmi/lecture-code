package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {

    public static void main (String[]args){
        System.out.println("Operasi penjumlahan");
        int hasilpenjumlahan =5+4;
        System.out.println("Hasil 5 + 4 = " + hasilpenjumlahan);
        System.out.println();

        System.out.println("Operasi pengurangan");
        int hasilpengurangan = 5-4;
        System.out.println("Hasil 5-4 = " + hasilpengurangan);
        System.out.println();

        System.out.println("Operasi pengalian");
        int hasilpengalian = 5*4;
        System.out.println("Hasil 5*4 = " + hasilpengalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilpembagian = 5/4;
        System.out.println("Hasil 5/4 = " + hasilpembagian);
        System.out.println();

        System.out.println("Operasi habis bagi");
        int hasilsisa1 = 8%2;
        System.out.println("Hasil 8 % 2 = " + hasilsisa1);
        System.out.println();
        int hasilsisa2 = 9 % 2;
        System.out.println("Hasil 9 % 2 = " + hasilsisa2);
        System.out.println();
    }
}

